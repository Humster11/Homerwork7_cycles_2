public class Task2 {
    public static void main(String[] args) {
        int i = 1;

        while (i <= 10) {
            System.out.print(i + " ");
            i = i + 1;
        }
        System.out.print("\n");

        for (int j = 10; j >= 1; j--) {
            System.out.print(j + " ");
        }
    }
}
